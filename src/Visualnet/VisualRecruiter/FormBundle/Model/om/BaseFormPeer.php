	}

	/**
	 * Retrieve object using using composite pkey values.
	 * @param      int $id
	 * @param      int $user_id
	 * @param      int $group_id
	 * @param      PropelPDO $con
	 * @return     Form
	 */
	public static function retrieveByPK($id, $user_id, $group_id, PropelPDO $con = null) {
		$_instancePoolKey = serialize(array((string) $id, (string) $user_id, (string) $group_id));
 		if (null !== ($obj = FormPeer::getInstanceFromPool($_instancePoolKey))) {
 			return $obj;
		}

		if ($con === null) {
			$con = Propel::getConnection(FormPeer::DATABASE_NAME, Propel::CONNECTION_READ);
		}
		$criteria = new Criteria(FormPeer::DATABASE_NAME);
		$criteria->add(FormPeer::ID, $id);
		$criteria->add(FormPeer::USER_ID, $user_id);
		$criteria->add(FormPeer::GROUP_ID, $group_id);
		$v = FormPeer::doSelect($criteria, $con);

		return !empty($v) ? $v[0] : null;
	}
